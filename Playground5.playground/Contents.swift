
// + operation
func plus(firstNum: Int, secondNum: Int) -> Int {
    return firstNum + secondNum
}

var added = plus(firstNum: 50, secondNum: 10)
print("The two numbers added = \(added)")


// - operation
func minus(firstNum: Int, secondNum: Int) -> Int {
    return firstNum - secondNum
}

var subtracted = minus(firstNum: 50, secondNum: 10)
print("The two numbers subtracted = \(subtracted)")


// / operation
func division(firstNum: Int, secondNum: Int) -> Int {
    return firstNum / secondNum
}


var divided = division(firstNum: 50, secondNum: 10)
print("The two numbers divided = \(divided)")


// * operation
func multiplication(firstNum: Int, secondNum: Int) -> Int {
    return firstNum * secondNum
}

var multiplied = multiplication(firstNum: 50, secondNum: 10)
print("The two numbers multiplied = \(multiplied)")

